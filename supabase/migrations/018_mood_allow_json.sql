-- ============================================================
-- 018: mood カラムの CHECK 制約を削除し、JSON配列文字列も受け付けるよう変更
--
-- 【背景】
-- mood カラムは元々 '順調' など単一値のみを受け付ける CHECK 制約があったが、
-- 「今の状態」が複数選択に対応したことで JSON.stringify() した文字列
-- （例: '["順調","励ましがほしい"]'）も保存する必要がある。
-- CHECK 制約を外して text のみに変更する。
-- ※ 既存の単一値データはそのまま読み込める（parseMoods が JSON.parse を試みて
--    失敗した場合は単一値として扱うフォールバック処理が実装済み）
-- ============================================================

-- 既存の CHECK 制約を削除
ALTER TABLE public.checkins
  DROP CONSTRAINT IF EXISTS checkins_mood_check;

-- DEFAULT は '順調' のまま維持（単一値後方互換）
-- カラム型は text のまま変更不要
-- JSON配列文字列（例: '["順調","励ましがほしい"]'）もそのまま格納可能になる

COMMENT ON COLUMN public.checkins.mood
  IS '気分・状態。単一値（後方互換）または JSON 配列文字列（複数選択時）を格納。'
  || '例: ''順調'' または ''["順調","励ましがほしい"]''';
